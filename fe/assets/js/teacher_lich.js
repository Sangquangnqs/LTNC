function toggleDropdown() {
    var dropdownContent = document.getElementById("dropdownContent");
    if (dropdownContent.style.display === "none" || dropdownContent.style.display === "") {
        dropdownContent.style.display = "flex";
    } else {
        dropdownContent.style.display = "none";
    }
}


function toggleDefault() {
    var defaultContent = document.querySelector('.macdinh');
    var calendarContent = document.querySelector('.calendar');
    var haiContent = document.querySelector('.hai');
    var baContent = document.querySelector('.ba');
    var tuContent = document.querySelector('.tu');
    var namContent = document.querySelector('.nam');
    var sauContent = document.querySelector('.sau');
    var bayContent = document.querySelector('.bay');
    var cnContent = document.querySelector('.cn');

    defaultContent.style.display = 'block';
    calendarContent.style.display = 'none';
    haiContent.style.display = 'none';
    baContent.style.display = 'none';
    tuContent.style.display = 'none';
    namContent.style.display = 'none';
    sauContent.style.display = 'none';
    bayContent.style.display = 'none';
    cnContent.style.display = 'none';
}

